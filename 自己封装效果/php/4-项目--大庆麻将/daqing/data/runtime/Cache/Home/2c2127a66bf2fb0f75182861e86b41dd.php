    <p>（3）强制离线：强制让违规用户离开当前游戏，结束该用户当前游戏程序的执行。</p>
    <p>（4）封停帐号：暂停或永久终止违规用户使用37游戏帐号登录某款游戏的权利。</p>
    <p>（5）暂时隔离：将违规用户的游戏角色转移至特殊游戏场景，限制其局部游戏操作，直到此次处罚到期或是取消。</p>
    <p>（6）删除档案：将违规用户在某个游戏世界中的人物档案删除，不让该人物再出现在游戏世界。</p>
