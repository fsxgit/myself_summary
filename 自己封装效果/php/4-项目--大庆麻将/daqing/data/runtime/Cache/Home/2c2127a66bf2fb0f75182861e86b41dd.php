    <p>（5）其他危害计算机信息网络安全的行为。</p>
    <p>12. 用户同意以游戏程序中的监测数据作为判断用户是否有通过使用外挂程序等方法进行的游戏作弊行为的依据。</p>
    <p>13. 如果大庆麻将发现用户行为或者数据异常，可以观察及记录该用户行为，并以观察和记录的结果作为判断用户是否实施了违反本协议用户行为守则的依据。</p>
    <p>37游戏积极保护用户的帐号、虚拟物品及虚拟货币的安全，为此，大庆麻将对盗号及盗号相关行为展开严厉的打击。大庆麻将发现或者怀疑存在包括但不限于以下的盗号及盗号相关行为时，有权视情况按照本协议第六条第14款、第15款、第十四条及其他相关条款的规定处理，同时，37游戏保留进一步追诉的权利：（1）盗取帐号；（2）盗取虚拟物品；（3）盗取虚拟货币；（4）盗取帐号或/及密码；（5）异常IP下的物品转移；（6）其他盗号及盗号相关行为。为了维护游戏的公平与秩序，即使用户没有主动的参与盗号，但是用户的物品来源于盗号或者盗号相关行为的，大庆麻将也有权自行判断回收、冻结涉及盗号的物品及帐号。用户应该配合大庆麻将对盗号及盗号相关行为的调查。用户应该自觉维护游戏的秩序，大庆麻将发现或者怀疑存在虚假的盗号投诉时，有权视情况按照本协议第六条第14款、第15款、第十四条及其他相关条款的规定处理。</p>
